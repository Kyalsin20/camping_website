// Add to cart function
const addToCart = (item) => {
  const addedItems = JSON.parse(localStorage.getItem("addedItems")) || [];

  const existingItem = addedItems.find((addedItem) => addedItem.id === item.id);

  if (!existingItem) {
    addedItems.push(item);

    localStorage.setItem("addedItems", JSON.stringify(addedItems));

    showCustomAlert("Item added successfully to cart!");
  } else {
    showCustomAlert("You have already added this item in cart!");
  }
};

